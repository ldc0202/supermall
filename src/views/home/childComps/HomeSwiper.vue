<template>
  <swiper>
         <swiper-item v-for="item in banners" :key="item.title">
           <a :href="item.link">
             <img :src="item.image" alt="" @load="imagload">
           </a>
         </swiper-item>
       </swiper>
</template>

<script>
import {Swiper,SwiperItem} from 'components/common/swiper'
export default {
  data(){
    return{
      isLoad:false
    }
  },
    props:{
        banners:{
            type:Array,
            default(){
                return[]
            }
        }
    }, 
    methods:{
      imagload(){
        // console.log('----')
        if(!this.isLoad){
          this.$emit('swiperImageLoad')
          this.isLoad=true
        }
      }
    },
   components:{
       Swiper,
       SwiperItem
   }
}
</script>

